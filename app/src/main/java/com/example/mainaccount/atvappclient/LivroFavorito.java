package com.example.mainaccount.atvappclient;

public class LivroFavorito {

    private String userCodigo;
    private String key;
    private String livro;
    private String autor;

    public String getUserCodigo() { return userCodigo; }

    public void setUserCodigo(String userCodigo) { this.userCodigo = userCodigo; }

    public String getKey() { return key; }

    public void setKey(String key) { this.key = key; }

    public String getLivro() {
        return livro;
    }

    public void setLivro(String livro) {
        this.livro = livro;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

}
